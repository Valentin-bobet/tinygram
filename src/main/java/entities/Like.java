package entities;

public class Like {

	public String mail;
	public String postLiked;

	public Like() {}

	public String getMail() {
		return mail;
	}

	public void setMail(String mail) {
		this.mail = mail;
	}

	public String getPostLiked() {
		return postLiked;
	}

	public void setPostLiked(String postLiked) {
		this.postLiked = postLiked;
	}

}
